def find_nearest_broken_pumpkin():
	map_size = 32
	min_distance = None
	nearest_pos = None
	current_x = get_pos_x()
	current_y = get_pos_y()
	# this method costs O(n^2), it's too slow for large map
	# for x in range(map_size):
	# 	for y in range(map_size):
	# 		if pumpkin_broken_matrix[x][y] == 1:
	# 			distance = abs(current_x - x) + abs(current_y - y)
	# 			if min_distance is None or distance < min_distance:
	# 				min_distance = distance
	# 				nearest_pos = (x, y)
	#
	# for optimization, we just scan in increasing distance rings
    # pumpkin needs time to grow, so we need to record a cooldown time before rechecking the same position(not implemented yet)
	for distance in range(1, map_size * 2):
		for dx in range(-distance, distance + 1):
			dy = distance - abs(dx)
			for sign in [-1, 1]:
				x = current_x + dx
				y = current_y + sign * dy
				if 0 <= x < map_size and 0 <= y < map_size:
					if pumpkin_broken_matrix[x][y] == 1:
						nearest_pos = (x, y)
						return nearest_pos
	return nearest_pos

def replace_broken_pumpkin(x, y):
	position.move_to(x, y)
	if not get_entity_type() == Entities.Pumpkin:
		operate.operate(Entities.Pumpkin)
		return False
	if not can_harvest():
		pumpkin_broken_matrix[x][y] = 1 # broken in reality
		operate.operate(Entities.Pumpkin)
		return False
	else:
		pumpkin_broken_matrix[x][y] = 0 # good in reality

if __name__ == "__main__":
	while 1:
		# plant pumpkin in full area
		map_size = 32
		position.move_to(0, 0)
		while True:
			# find broken pumpkins in pumpkin_broken_matrix and replant
			nearest = find_nearest_broken_pumpkin()
			replace_broken_pumpkin(nearest[0], nearest[1])
			if nearest == None:
				harvest()
				break
